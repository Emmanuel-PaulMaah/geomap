export function getCountryQID(cca3) {
  return countryQIDs[cca3] || null;
}

export function getCountryQIDByName(countryName) {
  // Try to find by name in values, otherwise use mapping
  const found = Object.entries(countryQIDs).find(
    ([_, qid]) => qid && countryName.toLowerCase().includes(_) || _.toLowerCase().includes(countryName.toLowerCase())
  );
  return found ? found[1] : null;
}
